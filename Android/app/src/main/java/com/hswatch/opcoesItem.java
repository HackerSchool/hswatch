package com.hswatch;

public class opcoesItem {

    public int opImagem;
    public String opTexto;

    public opcoesItem(int opImagem, String opTexto) {
        this.opImagem = opImagem; this.opTexto = opTexto;
    }

    public int getOpImagem() {
        return opImagem;
    }

    public String getOpTexto() {
        return opTexto;
    }
}
